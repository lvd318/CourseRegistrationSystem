    public void LoadData(){
        RegisterSessionDAO std = new RegisterSessionDAO();
        DefaultTableModel dtm = (DefaultTableModel) tblSession.getModel();
        dtm.setNumRows(0);
        //SimpleDateFormat fm = new SimpleDateFormat("MM/dd/yyyy"); 
        for(RegisterSession st : std.findAll()){
            dtm.addRow(new Object[]{st.getSessionId(), st.getSemNameFk(), st.getYearFk(),
                            fm.format(st.getStartDay()), fm.format(st.getEndDay())});
        }
        //this.tblSemesters.setModel(dtm);
    }
    
