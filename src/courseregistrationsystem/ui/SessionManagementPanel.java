    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        RegisterSessionDAO sbdao = new RegisterSessionDAO();
        RegisterSession st = new RegisterSession();
        SemesterDAO sdao = new SemesterDAO();
        Semester semester = null;
        
        for(Semester t : sdao.findAll()){
            if (t.isCurrentSem()){
                //semester = sdao.findSemester(t.getId());
                semester = t;
            }
        }

        st.setSemNameFk(semester.getId().getSemName());
        st.setYearFk(semester.getId().getYear());
        
        //Integer.parseInt(txtYear.getText()

        try {
            st.setStartDay(fm.parse(fm.format(StartDate.getDate())));
            st.setEndDay(fm.parse(fm.format(EndDate.getDate())));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        if (sbdao.saveRegisterSession(st)) {
            JOptionPane.showMessageDialog(null, "Tạo 1 đợt ĐKHP thành công");
            LoadData();
        } else {
            JOptionPane.showMessageDialog(null, "Thêm hất bại");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

