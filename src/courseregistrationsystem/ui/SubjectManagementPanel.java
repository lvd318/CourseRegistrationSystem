    public boolean validateForm(){
        if(txtSubjectId.getText().isEmpty() || txtSubjectName.getText().isEmpty() || txtCredits.getText().isEmpty()){
            return false;
        }
        return true;
    }
    
    private void tblSubjectsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSubjectsMouseClicked
        SubjectDAO std = new SubjectDAO();
        try{
            int id = tblSubjects.getSelectedRow();
            String subjectId = tblSubjects.getValueAt(id, 0).toString();
            Subject st = std.findSubject(subjectId);
            txtSubjectId.setText(st.getSubjectId());
            txtCredits.setText(String.valueOf(st.getCredits()));
            txtSubjectName.setText(st.getSubjectName());
        }catch(Exception e){
        }
    }//GEN-LAST:event_tblSubjectsMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(!txtSubjectId.getText().isEmpty()){
            SubjectDAO sbd = new SubjectDAO();
            Subject sb = sbd.findSubject(txtSubjectId.getText());

            if(sbd.deleteSubject(sb)){
                JOptionPane.showMessageDialog(null, "Xóa môn học thành công");
                LoadData();
            }
            else {
                JOptionPane.showMessageDialog(null, "Xóa thất bại");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mã MH cần xóa");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(validateForm()){
            SubjectDAO stdao = new SubjectDAO();
            Subject st = new Subject();
            st.setSubjectId(this.txtSubjectId.getText());
            st.setSubjectName(this.txtSubjectName.getText());
            st.setCredits(Integer.parseInt(this.txtCredits.getText()));
            if(stdao.updateSubject(st)){
                JOptionPane.showMessageDialog(null, "Môn học đã được cập nhật");
                LoadData();
            }
            else {
                JOptionPane.showMessageDialog(null, "Cập nhật thất bại");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Nhập đầy đủ thông tin để cập nhật");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

