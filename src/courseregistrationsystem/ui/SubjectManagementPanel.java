    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(validateForm()){
            SubjectDAO sbdao = new SubjectDAO();
            Subject sb = new Subject();
            sb.setSubjectId(this.txtSubjectId.getText());
            sb.setSubjectName(this.txtSubjectName.getText());
            sb.setCredits(Integer.parseInt(this.txtCredits.getText()));
            if(sbdao.saveSubject(sb)){
                JOptionPane.showMessageDialog(null, "Thêm 1 môn học thành công");
                LoadData();
            }
            else {
                JOptionPane.showMessageDialog(null, "Thêm môn học thất bại");
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Vui lòng nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        SubjectDAO sbd = new SubjectDAO();
        if(txtSubjectId.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Mã môn học cần phải nhập để tìm kiếm");
        }
        else{
            try{
                Subject sb = sbd.findSubject(txtSubjectId.getText());

                if(sb != null){
                    txtSubjectId.setText(sb.getSubjectId());
                    txtCredits.setText(String.valueOf(sb.getCredits()));
                    txtSubjectName.setText(sb.getSubjectName());
                    //Fill Data in table
                    DefaultTableModel dtm = new DefaultTableModel();
                    dtm.addColumn("Mã môn học");
                    dtm.addColumn("Tên môn học");
                    dtm.addColumn("Số tín chỉ");
                    dtm.addRow(new Object[]{sb.getSubjectId(), sb.getSubjectName(), sb.getCredits()});
                    this.tblSubjects.setModel(dtm);
                }else
                JOptionPane.showMessageDialog(null,"Không tìm thấy môn học này");
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnFindActionPerformed

